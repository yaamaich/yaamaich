#include "push_swap.h"

void	free_stack(t_stack **a)
{
	t_stack	*tmp;

	if (!a || !(*a))
		return ;
	while ((*a) != NULL)
	{
		tmp = (*a);
		*a = (*a)->next;
		free(tmp);
	}
}
