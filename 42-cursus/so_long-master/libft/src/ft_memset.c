#include "../include/libft.h"

void	*ft_memset(void *location, int byte, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		((char *)location)[i] = (unsigned char)byte;
		i++;
	}
	return (location);
}
