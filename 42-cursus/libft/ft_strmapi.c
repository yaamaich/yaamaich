#include "libft.h"

 char *ft_strmapi(const char *s, char (*f)(unsigned int, char))
 {
	size_t i;
	char *p;
	
	i = 0;
	if (!s)
		return (NULL);
	if (!f)
		return (NULL);
	p = malloc (ft_strlen(s) + 1);
	if (!p)
		return (0);
	while (s[i])
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
	
 }