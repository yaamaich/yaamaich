#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		l;
	char	*dest;

	i = 0;
	l = ft_strlen(s);
	dest = (char *)malloc(l + 1);
	if (!dest)
		return (NULL);
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
