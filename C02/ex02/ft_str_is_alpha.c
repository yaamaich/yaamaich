#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z') 
			|| (str[i] >= 'a' && str[i] <= 'z'))
		{
			i++;
			j++;
		}
		else 
		{
			j = 0;
			break ;
		}
	}
	if (j == 0)
	{
		return (0);
	}
	return (1);
}
