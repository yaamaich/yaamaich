int	ft_str_is_numeric(char *str)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			i++;
			j++;
		}
		else 
		{
			j = 0;
			break ;
		}
	}
	if (j == 0)
	{
		return (0);
	}
	return (1);
}
